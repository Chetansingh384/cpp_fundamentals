#include <iostream>
using namespace std;

int main() {
    int i = 10;           // Integer
    float f = 3.14f;      // Float
    double d = 3.14159;   // Double
    char c = 'A';         // Character
    bool b = true;        // Boolean

    cout << "int: " << i << "\nfloat: " << f << "\ndouble: " << d
         << "\nchar: " << c << "\nbool: " << b << endl;

    return 0;
}
